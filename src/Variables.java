public class Variables {
    public static void main(String[] args) {
        // Integer variable
        int age = 21;

        // Double (decimal number)
        double salary = 1000000.00;

        // Character
        char grade = 'A';

        // Boolean (true/false)
        boolean istrue = true;

        // String (text)
        String name = "Ashish";

        // Output eg
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Salary: Rs." + salary);
        System.out.println("Grade: " + grade);
        System.out.println("Is Java a Object-Oriented programming language? " + istrue);
        //Java is based on OOP principles, everything is made using objects and classes.
    }
}
